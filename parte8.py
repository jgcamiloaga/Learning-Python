#Con un argumento
for i in range(10):
    print("El valor de i es", i)
#El ultimo número será 10-1 = 9


#Con dos argumentos
for i in range(2, 8):
    print("El valor de i es", i)
#El primer número será 2
#EL último número será 8-1 = 7


#Con 3 argumentos
for i in range(2, 8, 3):
    print("El valor de i es", i)
#El primer número será 2
#Los números irán aumentando de 3 en 3
#Es decir, 2+3 = 5, 5+3 = 8
#Se imprimirán los números 2 y 5, pero no el 8, ya que es igual al límite.
#El último número será 5.

